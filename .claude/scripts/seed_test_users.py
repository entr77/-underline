def build_catalog():
    """Search Kakao Books for each category, return {cat: [book_dict, ...]} with covers only."""
    catalog = {}
    for cat, queries in CATEGORY_QUERIES.items():
        seen = set()
        books = []
        for q in queries:
            try:
                results = search_kakao(q, size=15)
                for b in results:
                    if b["kakao_id"] not in seen:
                        seen.add(b["kakao_id"])
                        books.append(b)
            except Exception as e:
                print(f"  [warn] Kakao search failed for {q!r}: {e}")
            if len(books) >= 15:
                break
        print(f"  {cat}: {len(books)} books found")
        catalog[cat] = books[:15]
    return catalog

def upsert_book(book):
    """Upsert book into public.books, return UUID."""
    payload = {
        "kakao_id":  book["kakao_id"],
        "title":     book["title"][:200],
        "author":    book["author"][:200],
        "publisher": book.get("publisher", "")[:200],
        "cover_url": book.get("cover_url", ""),
        "isbn":      book.get("isbn", ""),
    }
    r = requests.post(
        f"{REST}/books",
        headers={**H, "Prefer": "resolution=merge-duplicates,return=representation"},
        params={"on_conflict": "kakao_id"},
        json=payload,
    )
    r.raise_for_status()
    return r.json()[0]["id"]

def create_user(u):
    """Create auth user, patch profile, return uuid."""
    uid = None
