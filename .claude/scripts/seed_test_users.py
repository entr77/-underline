def run_seed():
    print("\n[1] Building book catalog from Kakao API …")
    catalog = build_catalog()

    print("\n[2] Upserting books …")
    book_ids = {}  # category -> [book_uuid, ...]
    for cat, books in catalog.items():
        book_ids[cat] = []
        for b in books:
            try:
                bid = upsert_book(b)
                book_ids[cat].append((bid, b["kakao_id"]))
            except Exception as e:
                print(f"  [warn] book upsert failed: {b['title']!r}: {e}")

    print("\n[3] Creating users and inserting underlines …")
    total_underlines = 0
    for u in USERS:
        cat = u["category"]
        cat_books = book_ids.get(cat, [])
        if not cat_books:
            print(f"  [skip] no books for {cat}")
            continue
        try:
            uid = create_user(u)
        except Exception as e:
            print(f"  [error] {u['username']}: {e}")
            continue

        # Pick 5 books (or as many as available), deterministic by user idx
        n = len(cat_books)
        selected = [cat_books[(u["idx"] + i) % n] for i in range(min(5, n))]

        # Pick 7 quotes per book from category pool
        pool = QUOTES.get(cat, [])
        if not pool:
            print(f"  [warn] no quotes for {cat}")
            continue

        ul_count = 0
        for (book_uuid, _) in selected:
            offset = (u["idx"] * 3) % len(pool)
            quotes = [pool[(offset + j) % len(pool)] for j in range(7)]
            try:
                insert_underlines(uid, book_uuid, quotes, offset)
                ul_count += 7
            except Exception as e:
                print(f"    [warn] underline insert failed: {e}")

        total_underlines += ul_count
        print(f"  ✓ {u['username']} ({cat}): {len(selected)} books, {ul_count} underlines")

    print(f"\nDone. Total underlines: {total_underlines}")

def run_cleanup():
    """Delete all seed test users in safe order to avoid trigger FK violations."""
    print("Fetching auth users …")
    users = aget_users()
    seed_users = [u for u in users if u.get("email", "").endswith(("@seed.test", "@underline.test"))]
    print(f"Found {len(seed_users)} seed users to delete …")
    for u in seed_users:
        uid = u["id"]
        try:
            # Delete in safe order: underlines first (triggers fire while user exists),
            # then taste_profiles, then public.users, then auth.users
            requests.delete(f"{REST}/likes",               headers=H, params={"user_id": f"eq.{uid}"})
            requests.delete(f"{REST}/underlines",          headers=H, params={"user_id": f"eq.{uid}"})
            requests.delete(f"{REST}/user_taste_profiles", headers=H, params={"user_id": f"eq.{uid}"})
            requests.delete(f"{REST}/users",               headers=H, params={"id":      f"eq.{uid}"})
            adelete(uid)
            print(f"  ✓ deleted: {u['email']}")
        except Exception as e:
            print(f"  [error] {u['email']}: {e}")
        time.sleep(0.05)
    print("Cleanup done.")

def run_fix_covers():
    """Delete books without cover_url and their underlines."""
    print("Finding books without cover_url …")
    r = requests.get(
        f"{REST}/books",
        headers=H,
        params={"cover_url": "eq.", "select": "id,title"},
    )
    r.raise_for_status()
    books = r.json()
    # Also get null covers
    r2 = requests.get(
        f"{REST}/books",
        headers={**H, "prefer": ""},
        params={"cover_url": "is.null", "select": "id,title"},
    )
    r2.raise_for_status()
    books += r2.json()
    if not books:
        print("No books without covers found.")
        return
    print(f"Found {len(books)} books without covers")
    for b in books:
        bid = b["id"]
        # Delete underlines first (FK)
        requests.delete(f"{REST}/underlines", headers=H, params={"book_id": f"eq.{bid}"}).raise_for_status()
        requests.delete(f"{REST}/books", headers=H, params={"id": f"eq.{bid}"}).raise_for_status()
        print(f"  deleted: {b['title']!r}")
    print("Fix covers done.")

def run_fix_themes():
    """Update all existing underlines with random card themes (paginated)."""
    print("Fetching all underlines …")
    ids = []
    offset = 0
    page = 1000
    while True:
        r = requests.get(
            f"{REST}/underlines",
            headers={**H, "Range-Unit": "items", "Range": f"{offset}-{offset+page-1}"},
            params={"select": "id"},
        )
        r.raise_for_status()
        batch = [row["id"] for row in r.json()]
        ids.extend(batch)
        if len(batch) < page:
            break
        offset += page
    print(f"Updating {len(ids)} underlines with random themes …")
    for uid in ids:
        t = random_theme()
        requests.patch(
            f"{REST}/underlines",
            headers=H,
            params={"id": f"eq.{uid}"},
            json=t,
        ).raise_for_status()
    print(f"Done. {len(ids)} underlines updated.")

