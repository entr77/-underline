def rpost(path, data, params=None):
    h = {**H, "Prefer": "return=representation"}
    r = requests.post(f"{REST}/{path}", headers=h, json=data, params=params)
    r.raise_for_status(); return r.json()

def rpatch(path, data):
    r = requests.patch(f"{REST}/{path}", headers=H, json=data)
    r.raise_for_status()

