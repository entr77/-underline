def search_kakao(query, size=15):
    r = requests.get(
        "https://dapi.kakao.com/v3/search/book",
        headers={"Authorization": f"KakaoAK {KAKAO_KEY}"},
        params={"query": query, "size": size, "target": "title"},
        timeout=10,
    )
    r.raise_for_status()
    docs = r.json().get("documents", [])
    out = []
    for d in docs:
        thumb = d.get("thumbnail", "").strip()
        if not thumb:
            continue
        isbn_list = d.get("isbn", "").split()
        kakao_id = isbn_list[-1] if isbn_list else d.get("isbn", "")
        if not kakao_id:
            continue
        out.append({
            "kakao_id":  kakao_id,
            "title":     d.get("title", ""),
            "author":    ", ".join(d.get("authors", [])),
            "publisher": d.get("publisher", ""),
            "cover_url": thumb,
            "isbn":      kakao_id,
        })
    return out

